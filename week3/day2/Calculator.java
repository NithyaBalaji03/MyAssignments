package week3.day2;

public class Calculator {

	public int add(int intNum1, int intNum2, int intNum3) {

		return (intNum1 + intNum2 + intNum3);

	}

	public int sub(int intNum1, int intNum2) {

		return (intNum1 - intNum2);

	}

	public double mul(double dbNum1, double dbNum2) {

		return (dbNum1 * dbNum2);
		
	}

	public float divide(float flNum1, float flNum2) {

		return flNum1/flNum2;
		
	}
}
